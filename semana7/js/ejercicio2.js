// Programa que calcula el area de un triangulo rectangulo

let baseTrangulo = parseFloat(prompt("Ingrese la base del triángulo: "));
    
let alturaTrangulo = parseFloat(prompt("Ingrese la altura del triángulo: "));
    
let areaTriangulo = (baseTrangulo * alturaTrangulo) / 2;

alert(`El área del triángulo rectángulo es: ${areaTriangulo}`);